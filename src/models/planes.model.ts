import {Entity, model, property, belongsTo} from '@loopback/repository';
import {Mascotas} from './mascotas.model';

@model()
export class Planes extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  nombre: string;

  @property({
    type: 'number',
    required: true,
  })
  precio: number;

  @property({
    type: 'string',
    required: true,
  })
  descripcion: string;

  @property({
    type: 'string',
    required: true,
  })
  fecha: string;

  @property({
    type: 'string',
    required: true,
  })
  fin: string;

  @property({
    type: 'number',
    required: true,
  })
  estado: number;

  @belongsTo(() => Mascotas)
  mascotasId: string;

  constructor(data?: Partial<Planes>) {
    super(data);
  }
}

export interface PlanesRelations {
  // describe navigational properties here
}

export type PlanesWithRelations = Planes & PlanesRelations;
